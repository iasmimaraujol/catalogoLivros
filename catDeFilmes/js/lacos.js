//!montando a tabuada com o for
for(let i = 0; i <= 10; i++){
    for(let j = 0; j <= 10; j++){
        console.log(`A tabuada do ${i} é: ${i} * ${j} = ${i*j}`);
    }
    console.log('--------------');
}
// ctrl + ; para comentar vários
let a = 0
while(a < 0){
    console.log(a)
    a++
}
console.log('---');
do{
    console.log(a);
    a--
}while(a > 0)